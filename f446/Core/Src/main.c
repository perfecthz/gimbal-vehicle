    MPU6050_Init(&hi2c1);
    HAL_Delay(500);
    get_Zero_Offset(&hi2c1, &MPU_data);

    // Initializes OLED
    ssd1306_Init();
    ssd1306_Clear();
    ssd1306_SetColor(White);
